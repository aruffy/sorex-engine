TEST(StringUtils, ToInteger)
{
  String  str;
  WString wstr;

  int32  val32;
  uint32 valu32;
  int8   val8;
  int16  val16;

  EStatusCode status;

  // signed
  for (const int32 n : kSetRandom_32b)
  {
    str    = Utils::ToString(n);
    status = Utils::ToInteger(str, val32);
    EXPECT_EQ(status, EStatusCode::Ok);
    ASSERT_EQ(val32, n);

    wstr   = Utils::ToWString(n);
    status = Utils::ToInteger(wstr, val32);
    EXPECT_EQ(status, EStatusCode::Ok);
    ASSERT_EQ(val32, n);
  }
  // unsigned
  for (const uint32 n : kSetUnsigned_32b)
  {
    str    = Utils::ToString(n);
    status = Utils::ToInteger(str, valu32);
    EXPECT_EQ(status, EStatusCode::No_Error);
    ASSERT_EQ(valu32, n);

    wstr   = Utils::ToWString(n);
    status = Utils::ToInteger(wstr, valu32);
    EXPECT_EQ(status, EStatusCode::No_Error);
    ASSERT_EQ(valu32, n);
  }
  // int8
  for (const int8 n : kSetSigned_8b)
  {
    str    = Utils::ToString(n);
    status = Utils::ToInteger(str, val8);
    EXPECT_EQ(status, EStatusCode::No_Error);
    ASSERT_EQ(val8, n);

    wstr   = Utils::ToWString(n);
    status = Utils::ToInteger(wstr, val8);
    EXPECT_EQ(status, EStatusCode::No_Error);
    ASSERT_EQ(val8, n);
  }
  // int16
  for (const int16 n : kSetSigned_16b)
  {
    str    = Utils::ToString(n);
    status = Utils::ToInteger(str, val16);
    EXPECT_EQ(status, EStatusCode::No_Error);
    ASSERT_EQ(val16, n);

    wstr   = Utils::ToWString(n);
    status = Utils::ToInteger(wstr, val16);
    EXPECT_EQ(status, EStatusCode::No_Error);
    ASSERT_EQ(val16, n);
  }
  // check overflow
  int32           success       = 0;
  constexpr int16 kMaxSigned_8b = std::numeric_limits<int8>::max();
  constexpr int16 kMinSigned_8b = std::numeric_limits<int8>::min();
  for (const int16 n : kSetSigned_16b)
  {
    if (n > kMaxSigned_8b || n < kMinSigned_8b)
    {
      str    = Utils::ToString(n);
      status = Utils::ToInteger(str, val8);
      ASSERT_EQ(status, EStatusCode::No_Space)
        << "Str -> Value int16 to int8: " << n << " -> " << (int)val8;

      wstr   = Utils::ToWString(n);
      status = Utils::ToInteger(wstr, val8);
      ASSERT_EQ(status, EStatusCode::No_Space)
        << "WStr -> Value int16 to int8: " << n << " -> " << (int)val8;
    }
    else
    {
      str    = Utils::ToString(n);
      status = Utils::ToInteger(str, val8);
      EXPECT_EQ(status, EStatusCode::Ok);
      ASSERT_EQ(val8, n);

      wstr   = Utils::ToWString(n);
      status = Utils::ToInteger(wstr, val8);
      EXPECT_EQ(status, EStatusCode::Ok);
      ASSERT_EQ(val8, n);
      success++;
    }
  }
  // invalid string
  for (const String& s : kInvalidStringNumbers)
  {
    status = Utils::ToInteger(s, val32);
    EXPECT_EQ(status, EStatusCode::Invalid_Argument);
  }
}

TEST(StringUtils, ToFloat)
{
  String      str;
  float       fval;
  EStatusCode status;
  for (const float value : kFloatSet)
  {
    str    = Utils::ToString(value);
    status = Utils::ToFloat(str, fval);
    EXPECT_EQ(status, EStatusCode::Ok);
    ASSERT_EQ(fval, value);
    fval = 0.f;
    fval = Utils::ToFloat(str);
    ASSERT_EQ(fval, value);
    str.clear();
  }
}
